<?php
//Vòng lặp while
$dem = 0;
while ($dem < 5) {
    $dem++;
    echo "Vòng lặp while thứ: $dem<br>";
}

//Vòng lặp do .. while
do {
    $dem--;
    echo "Vòng lặp do .. while thứ: $dem<br>";
} while ($dem > 10);

//Vòng lặp for
for ($i = 0; $i < 5; $i++) {
    echo "Vòng lặp for thứ $i<br>";
}

